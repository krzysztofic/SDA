package blok1.weekend3.Zadania2_tydz3;

public class Zadanie1 {

    public static void main(String[] args) {

        for (int i = 1; i <=100 ; i++) {
            System.out.print(i+" ");
        }

        System.out.println("\n");

        int j=1;
        while(j <=100){
            System.out.print(j+" ");
            j++;
        }
    }
}
